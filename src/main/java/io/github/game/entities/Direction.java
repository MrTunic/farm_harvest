package io.github.game.entities;

/**
 * Represents the four cardinal movement directions for entities.
 */
public enum Direction {

    /** Facing downward (south). */
    DOWN,

    /** Facing left (west). */
    LEFT,

    /** Facing right (east). */
    RIGHT,

    /** Facing upward (north). */
    UP
}
